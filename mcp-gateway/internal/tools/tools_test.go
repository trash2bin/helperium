func TestDeriveParams_EdgeCases(t *testing.T) {
	entities := []config.Entity{
		{
			Name: "item", Fields: []config.EntityField{
				{Name: "id", Column: "id", Type: config.FieldTypeString},
			},
		},
	}

	// No path params, no entity
	ep := config.Endpoint{Op: config.OpFind, Entity: "item", SearchField: "name", QueryParam: "q"}
	ps := deriveParams(ep, entities)
	if len(ps) != 1 {
		t.Fatalf("len(params) = %d, want 1", len(ps))
	}
	if ps[0].Name != "q" {
		t.Errorf("param name = %q, want 'q'", ps[0].Name)
	}

	// No search_field, no query_param for find
	ep2 := config.Endpoint{Op: config.OpFind, Entity: "missing"}
	ps2 := deriveParams(ep2, entities)
	if len(ps2) != 0 {
		t.Errorf("len(params) = %d, want 0 (no entity found, no path params)", len(ps2))
	}

	// Multiple path params
	ep3 := config.Endpoint{Op: config.OpGetByID, Path: "/x/{a}/y/{b}", Entity: "item"}
	ps3 := deriveParams(ep3, entities)
	if len(ps3) != 2 {
		t.Fatalf("len(params) = %d, want 2", len(ps3))
	}
	if ps3[0].Name != "a" {
		t.Errorf("ps3[0].Name = %q, want 'a'", ps3[0].Name)
	}
	if ps3[1].Name != "b" {
		t.Errorf("ps3[1].Name = %q, want 'b'", ps3[1].Name)
	}
}

