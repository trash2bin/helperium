func TestRegistry_AutoGeneratedTools(t *testing.T) {
	r := NewRegistry(baseConfig())
	defs := r.GetToolDefs()

	expected := map[string]bool{
		"health":          true,
		"stats":           true,
		"get_student":     true,
		"find_student":    true,
		"list_discipline": true,
		"student_grades":  true,
	}
	if len(defs) != len(expected) {
		t.Fatalf("got %d tools, want %d\n  got: %v", len(defs), len(expected), toolNames(defs))
	}
	for _, td := range defs {
		if !expected[td.Name] {
			t.Errorf("unexpected tool %q", td.Name)
		}
		delete(expected, td.Name)
	}
	if len(expected) > 0 {
		t.Errorf("missing tools: %v", keys(expected))
	}

	// Verify endpoint field
	for _, td := range defs {
		if td.Endpoint == "" {
			t.Errorf("tool %q has empty Endpoint", td.Name)
		}
	}
}

func TestRegistry_ExplicitToolsOverride(t *testing.T) {
	cfg := baseConfig()
	cfg.MCPTools = []config.MCPTool{
		{
			Name:        "student_grades", // same name as auto-generated
			Endpoint:    "/grades",
			Description: "Custom grades tool",
		},
		{
			Name:        "brand_new_tool",
			Endpoint:    "/custom",
			Description: "Brand new",
		},
	}

	r := NewRegistry(cfg)
	defs := r.GetToolDefs()

	// student_grades should have overridden endpoint
	found := false
	for _, td := range defs {
		if td.Name == "student_grades" {
			found = true
			if td.Endpoint != "/grades" {
				t.Errorf("student_grades.Endpoint = %q, want %q", td.Endpoint, "/grades")
			}
			if td.Description != "Custom grades tool" {
				t.Errorf("student_grades.Description = %q, want %q", td.Description, "Custom grades tool")
			}
			break
		}
	}
	if !found {
		t.Error("student_grades not found after override")
	}

	// brand_new_tool should exist
	found = false
	for _, td := range defs {
		if td.Name == "brand_new_tool" {
			found = true
			break
		}
	}
	if !found {
		t.Error("brand_new_tool not found")
	}

	// Builtin tools should still exist
	builtins := []string{"health", "stats"}
	for _, name := range builtins {
		found = false
		for _, td := range defs {
			if td.Name == name {
				found = true
				break
			}
		}
		if !found {
			t.Errorf("builtin tool %q missing after override", name)
		}
	}
}

