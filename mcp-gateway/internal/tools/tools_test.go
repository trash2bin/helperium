func TestDeriveToolName_FromPath(t *testing.T) {
	// Without entity, picks entity name from path
	got := deriveToolName(config.Endpoint{Op: config.OpGetByID, Path: "/products/{id}"})
	if got != "get_products" {
		t.Errorf("got %q, want %q", got, "get_products")
	}

	// With entity
	got = deriveToolName(config.Endpoint{Op: config.OpList, Entity: "order"})
	if got != "list_order" {
		t.Errorf("got %q, want %q", got, "list_order")
	}

	// Path with no clear entity and no entity field (entity becomes {id})
	got = deriveToolName(config.Endpoint{Op: config.OpGetByID, Path: "/{id}"})
	if got != "get_{id}" {
		t.Errorf("got %q, want %q", got, "get_{id}")
	}
}

