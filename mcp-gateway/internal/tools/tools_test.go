func TestRegistry_DeriveParams_Path(t *testing.T) {
	r := NewRegistry(baseConfig())
	params := make(map[string][]config.EndpointParam)
	for _, td := range r.GetToolDefs() {
		params[td.Name] = td.Params
	}

	// get_student: {id} -> required path param
	ps := params["get_student"]
	if len(ps) != 1 {
		t.Fatalf("get_student: len(params) = %d, want 1", len(ps))
	}
	if ps[0].Name != "id" {
		t.Errorf("get_student param name = %q, want %q", ps[0].Name, "id")
	}
	if ps[0].In != config.ParamInPath {
		t.Errorf("get_student param in = %q, want %q", ps[0].In, config.ParamInPath)
	}
	if ps[0].Required == nil || !*ps[0].Required {
		t.Error("get_student param Required must be true")
	}

	// health: no params
	if len(params["health"]) != 0 {
		t.Errorf("health has %d params, want 0", len(params["health"]))
	}

	// stats: no params
	if len(params["stats"]) != 0 {
		t.Errorf("stats has %d params, want 0", len(params["stats"]))
	}
}

